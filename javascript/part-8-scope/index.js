// Global [used by default and var keyword]
// Global Scope : variables can be used throughout the code
// If you don't use any keyword while declaring variable
// default variable that javascipt make is global scope variable.
// Code here :
// {
//   x = 25;
// }
// console.log(x);
// {
//   var z = 27;
// }
// console.log(z);

// Local [used by let and const]
// Local Scope : Variable can not be used outside the curly braces
//               because they get destroyed outside the curly braces.
// Code here :
// {
//   let y = 29;
//   const w = 26;
// }
// console.log(w);
